// Solicita a base e a altura do triângulo ao usuário
var base = Number(prompt("Digite a base do triângulo:"));
var altura = Number(prompt("Digite a altura do triângulo:"));

// Calcula a área do triângulo
var area = (base * altura) / 2;

// Exibe a área do triângulo
alert(`A área do triângulo é: ${area}`);